    public int posicionHorario(int dia, int hora) {
        switch (hora) {
            case 7:
                hora = 0;
                break;
            case 9:
                hora = 1;
                break;
            case 11:
                hora = 2;
                break;
            case 14:
                hora = 3;
                break;
            case 16:
                hora = 4;
                break;
            case 18:
                hora = 5;
                break;
        }
        return dia + (5 * hora);
    }

